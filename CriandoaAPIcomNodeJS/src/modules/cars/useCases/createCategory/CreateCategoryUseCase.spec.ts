describe("Create Category", () => {
  it("Waiting the sum of 2 + 2 = 4", () => {
    const sum = 2 + 2;
    const result = 5;

    expect(sum).toBe(result);
  });

  it("Waiting the sum of 2 + 2 not to be 4", () => {
    const sum = 2 + 2;
    const result = 5;

    expect(sum).not.toBe(result);
  });
});
